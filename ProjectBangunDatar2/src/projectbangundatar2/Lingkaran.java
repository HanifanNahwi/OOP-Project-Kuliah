public class Lingkaran {
    // Hitung Luas
    double Luas(int r){
        return 3.14 * (r * r);
    }
    double Luas(double r){
        return 3.14 * (r * r);
    }
    
    // Hitung Keliling 
    double Keliling(int r){
        return 2 * 3.14 * r;
    }
    double Keliling(double r){
        return 2 * 3.14 * r;
    }
}
